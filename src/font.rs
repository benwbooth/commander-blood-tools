//! Game font glyphs + text drawing for the runnable engine — the 8x8 bitmap font
//! extracted from `BLOODPRG.EXE` (glyph map @0x14C22, advances @0x14CD2, rows
//! @0x14D28) plus the subtitle/text draw. Lib-accessible copy of the historically-
//! verified font in `src/extract/render.rs` (unify in a later pass).

pub const GAME_FONT_WIDTH: usize = 8;
pub const GAME_FONT_HEIGHT: usize = 8;
pub const GAME_FONT_LINE_HEIGHT: usize = 8;
pub const GAME_FONT_SPACE_ADVANCE: usize = 6;

#[derive(Clone, Copy, Debug, Eq, PartialEq)]
pub struct GameFontGlyph {
    pub rows: [u8; GAME_FONT_HEIGHT],
    pub advance: usize,
}

/// The glyph to blit for a subtitle character. Unknown glyphs fall back to '?'
/// so garbage is visible, BUT a space (' ') draws NOTHING (returns None) — the
/// game's `render_string` (BLOODPRG.EXE @0x31D7) skips the space glyph and only
/// advances DI by 6. Without this, `game_font_glyph(' ')` (None, no space glyph)
/// fell through to the '?' fallback and rendered every space as '?'. Verified
/// against a real playthrough (spaces were showing as '?' in generated subtitles).
pub fn subtitle_draw_glyph(ch: char) -> Option<GameFontGlyph> {
    if ch == ' ' {
        return None;
    }
    game_font_glyph(ch).or_else(|| game_font_glyph('?'))
}

pub fn game_font_glyph(ch: char) -> Option<GameFontGlyph> {
    let code = ch as usize;
    let idx = *GAME_FONT_CHAR_MAP.get(code)?;
    if idx == 0xff {
        return None;
    }
    let idx = idx as usize;
    Some(GameFontGlyph {
        rows: GAME_FONT_GLYPHS[idx],
        advance: GAME_FONT_WIDTHS[idx] as usize,
    })
}

pub fn game_font_advance(ch: char) -> usize {
    if ch == ' ' {
        return GAME_FONT_SPACE_ADVANCE;
    }
    game_font_glyph(ch)
        .or_else(|| game_font_glyph('?'))
        .map(|glyph| glyph.advance)
        .unwrap_or(GAME_FONT_SPACE_ADVANCE)
}


/// Draw `text` into a `width`-stride indexed framebuffer at (`x`,`y`) in `color`
/// using the game font (spaces advance without drawing). Clipped; returns end x.
pub fn draw_text_indexed(
    fb: &mut [u8],
    fb_width: usize,
    fb_height: usize,
    text: &str,
    x: usize,
    y: usize,
    color: u8,
) -> usize {
    let mut pen_x = x;
    for ch in text.chars() {
        if let Some(glyph) = subtitle_draw_glyph(ch) {
            for (gy, row) in glyph.rows.iter().enumerate() {
                let py = y + gy;
                if py >= fb_height {
                    continue;
                }
                for gx in 0..GAME_FONT_WIDTH {
                    if row & (0x80 >> gx) != 0 {
                        let px = pen_x + gx;
                        if px < fb_width {
                            fb[py * fb_width + px] = color;
                        }
                    }
                }
            }
        }
        pen_x += game_font_advance(ch);
    }
    pen_x
}

#[cfg(test)]
mod tests {
    use super::*;

    /// The extracted glyphs must match `BLOODPRG.EXE` byte-for-byte (rows @0x14D28,
    /// advances @0x14CD2, ASCII→index map @0x14C22) — this is what makes the subtitle
    /// font verified-by-construction against the game (no gameplay needed). Skips if the
    /// executable isn't present in this checkout.
    #[test]
    fn glyphs_match_bloodprg_exe_byte_for_byte() {
        let exe = match std::fs::read("re/bin/BLOODPRG.EXE")
            .or_else(|_| std::fs::read("../re/bin/BLOODPRG.EXE"))
        {
            Ok(b) => b,
            Err(_) => return,
        };
        let (map, adv, rows) = (0x14c22usize, 0x14cd2usize, 0x14d28usize);
        let space_idx = exe[map + b' ' as usize];
        let mut checked = 0;
        // Verify EVERY printable ASCII char that the exe maps to a non-space glyph -
        // uppercase, lowercase, digits, and punctuation - byte-for-byte.
        for c in 33u8..127 {
            let ch = c as char;
            let idx = exe[map + c as usize];
            if idx == space_idx {
                continue;
            }
            let g = game_font_glyph(ch)
                .unwrap_or_else(|| panic!("engine missing glyph for {ch:?} (exe idx {idx})"));
            checked += 1;
            let idx = idx as usize;
            assert_eq!(g.rows, exe[rows + idx * 8..rows + idx * 8 + 8], "glyph '{ch}' rows");
            assert_eq!(g.advance as u8, exe[adv + idx], "glyph '{ch}' advance");
        }
        assert_eq!(checked, 73, "expected 73 printable non-space glyphs incl. lowercase");
    }

    #[test]
    fn draws_glyphs_and_skips_spaces() {
        let (w, h) = (64usize, 12usize);
        let mut fb = vec![0u8; w * h];
        let end = draw_text_indexed(&mut fb, w, h, "HI", 0, 0, 7);
        assert!(fb.iter().any(|&p| p == 7));
        assert!(end > 0);
        let mut fb2 = vec![0u8; w * h];
        let after = draw_text_indexed(&mut fb2, w, h, " ", 0, 0, 7);
        assert!(fb2.iter().all(|&p| p == 0));
        assert_eq!(after, GAME_FONT_SPACE_ADVANCE);
    }
}
